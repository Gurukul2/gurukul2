# Funtional Programming (Increase reusability)#

def sum(a , b):
    add = a + b
    return add

def diff(a , b):
    sub = a - b
    return sub
operation = sum( 10, 20 )
print(operation)

operation = diff( 10, 20 )
print(operation)
